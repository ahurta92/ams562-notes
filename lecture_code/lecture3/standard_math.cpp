#include <cmath>
#include <iostream>

int main() {

  int a = -0b1011; // -11
  int c = 0b1000;  // 42944967285

  std::cout << " sin(pi) is = " << std::sin(M_PI) << std::endl;
  std::cout << " cos(0) is = " << std::cos(0.0) << std::endl;
  std::cout << " arcsin(1) is = " << std::asin(1.0) << std::endl;
  std::cout << " log 2 of base 2 is = " << std::log2(2) << std::endl;
  std::cout << " |-1| is = " << std::abs(-1) << std::endl;
}
